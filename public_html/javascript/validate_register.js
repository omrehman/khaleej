function validate_register(register)
{
	
	var id = register.id.value;
	var user = register.user.value;
	var full = register.full.value;
	var mob = register.mob.value;
	var addr = register.addr.value;
	if(id=="")
	{
		alert("enter id");
		return false;
	}

	if(user=="")
	{
		alert("enter user name");
		return false;
	}

	if(full=="")
	{
		alert("enter full name");
		return false;
	}

	if(mob=="")
	{
		alert("enter mobile no");
		return false;
	}

	if(addr=="")
	{
		alert("enter adress");
		return false;
	}
		return true;	
}
